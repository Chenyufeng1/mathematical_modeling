% 计算y随时间变化图
function calY(k)
A=(0.5888-0.256)/(k/0.34*0.0064);
B=sqrt((0.5888*9.8-1000*0.000256*9.8)/(k/0.34*0.0064));
V2=0.5888*sqrt(2*9.8*0)/0.8448;
C4=(-V2-B)/(B-V2);
t=linspace(0,1.0,100);
y=0.275+B*t-A*log(1-C4*exp(2*B*t/A))+A*log(1-C4);
y1=[26.1	25	23.5	21.5	19.4	17.5	14	11.5	8.5	5	2.8]/100;
t1=linspace(0.08,0.52,length(y1));
plot(t,y);
hold on;
plot(t1,y1,'r+');
xlabel('时间');
ylabel('竖直位置y');
title('大实心方体平放34速度0高度y随时间变化图');
